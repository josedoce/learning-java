package com.curso.java;

public class aula9 {
	public static void main(String[] args) {
		// = , -= , += , *= , /= , %=
		
		int salario = 0;
		
		salario = 1000;
		System.out.println(salario);
		
		salario -= 888;
		System.out.println(salario);
		
		salario += 100;
		System.out.println(salario);
		
		salario *= 1000;
		System.out.println(salario);
		
		salario /= 1000;
		System.out.println(salario);
	
		salario %= 1000;
		System.out.println(salario);
	}
}
